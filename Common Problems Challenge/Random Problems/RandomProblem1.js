const shortMaker = (arr) => {
  let length = arr.length;

  for (let i = 0; i < arr.length; i++) {
    if (arr[i] > 0) {
      length--;
    } else {
      return "invalid input!";
    }
  }

  if (length === 0) {
    //when we don't know how many numbers here, then we'll use loop! but we already know this conditionðŸ˜
    if (arr.at(0) === arr.at(1)) {
      return "equal!";
    } else {
      //bubble sort or simply built-in methods we can use both.

      // bubble sort
      for (let i = 0; arr.length > i; i++) {
        for (let j = i; arr.length > j; j++) {
          if (arr[j] > arr[i]) {
            let temp = arr[j];
            arr[j] = arr[i];
            arr[i] = temp;
          }
        }
      }

      //built-in methods
      return arr.sort().reverse();
    }
  }
  //when you use bubble sort otherwise no need 'return'
  return arr;
};

const arr = [1, -3];
